/**
 * 용어 조회 — 대소문자 무관.
 * ==용어== 패턴에서 추출한 텍스트로 조회.
 */
export function lookupGlossary(term: string): GlossaryEntry | undefined {
  // 정확히 일치
  if (GLOSSARY[term]) return GLOSSARY[term];
  // 대소문자 무관
  const lower = term.toLowerCase();
  for (const [key, value] of Object.entries(GLOSSARY)) {
    if (key.toLowerCase() === lower) return value;
  }
  // ko 필드로도 매칭 (한국어로 쓴 경우)
  for (const value of Object.values(GLOSSARY)) {
    if (value.ko.toLowerCase().includes(lower)) return value;
  }
  return undefined;
}
